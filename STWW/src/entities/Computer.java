package entities;

public class Computer {

	private int Cx;
	private int Cy;
	
	public Computer() {}
	public Computer(int Cx, int Cy) {
		this.Cx=Cx;
		this.Cy=Cy;
	}
	public int getCx() {
		return Cx;
	}
	public void setCx(int cx) {
		Cx = cx;
	}
	public int getCy() {
		return Cy;
	}
	public void setCy(int cy) {
		Cy = cy;
	}


		
	
	
}

